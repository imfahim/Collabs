    {
      // Login System
        $user = DB::table('users')
          ->where('email', $request->Email)
          ->where('password', $request->Pass)
          ->first();

        if($user){

          if($user->type == 1){
            Session::flash('success', 'Successfully Logged In!' );
            Session::push('id', $user->id);

            return redirect()->route('user.index');
          }



        }
    }

